
public class MyPOJO {

    private String createdBy;
    private String name;
    private String type;
    private String parent;
    private String child;
    private String isNew;
    private String testField;
    private String link;
    private String descriptor;
    private String another;
    private String field;
    private String age;
    private String department;
    private String amount;
    private String etc;

    public MyPOJO(String createdBy, String name, String type, String parent, String child, String isNew, String testField, String link, String descriptor, String another, String field, String age, String department, String amount, String etc) {
        this.createdBy = createdBy;
        this.name = name;
        this.type = type;
        this.parent = parent;
        this.child = child;
        this.isNew = isNew;
        this.testField = testField;
        this.link = link;
        this.descriptor = descriptor;
        this.another = another;
        this.field = field;
        this.age = age;
        this.department = department;
        this.amount = amount;
        this.etc = etc;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getParent() {
        return parent;
    }

    public String getChild() {
        return child;
    }

    public String getIsNew() {
        return isNew;
    }

    public String getTestField() {
        return testField;
    }

    public String getLink() {
        return link;
    }

    public String getDescriptor() {
        return descriptor;
    }

    public String getAnother() {
        return another;
    }

    public String getField() {
        return field;
    }

    public String getAge() {
        return age;
    }

    public String getDepartment() {
        return department;
    }

    public String getAmount() {
        return amount;
    }

    public String getEtc() {
        return etc;
    }

}
